/* OPERADORES aritimeticos

+ -> Adicao
- -> subtracao
* -> Multiplicacao
/ -> Divisao
% -> resto
++ -> incremento  a cada comando ++ ele adiciona uma unidade ao valor atribuido a variavel
-- -> decremento a cada comando -- ele subtrai uma unidade ao valor atribuido a variavel
** -> exponencial potenciacao
*/

let myNumber = 10
myNumber++
console.log(myNumber)
